function drawTipsPage(doc: jsPDF, data: TravelGuideData, cursor: PdfCursor) {
  doc.addPage()
  cursor.y = PDF.margin + 4
  drawPageHeader(doc, cursor)

  drawSectionTitle(
    doc,
    cursor,
    'tips',
    'Travel Tips',
    'Expert advice tailored to your destination and plans.',
  )

  data.travelTips.forEach((tip, index) => {
    ensureSpace(doc, cursor, 14)
    doc.setFont(PDF.fontMono, 'bold')
    doc.setFontSize(9)
    setText(doc, PDF.brass)
    doc.text(String(index + 1).padStart(2, '0'), PDF.margin, cursor.y)
    doc.setFont(PDF.fontBody, 'normal')
    doc.setFontSize(10)
    setText(doc, PDF.ink)
    cursor.y = wrapText(doc, tip, PDF.margin + 10, cursor.y, PDF.contentWidth - 12, 5) + 5
  })
}

function hexToRgb(hex: string): [number, number, number] {
  const normalized = hex.replace('#', '')
  const value = Number.parseInt(normalized, 16)
  return [(value >> 16) & 255, (value >> 8) & 255, value & 255]
}

export function buildTravelGuidePdf(data: TravelGuideData): jsPDF {
  const doc = new jsPDF({ unit: 'mm', format: 'a4', compress: true })
  const cursor: PdfCursor = { y: PDF.margin }

  drawCoverPage(doc, data)
  drawSummaryPage(doc, data, cursor)
  drawItineraryPages(doc, data, cursor)
  drawBudgetPage(doc, data, cursor)
  drawWeatherPage(doc, data, cursor)
  drawPackingPage(doc, data, cursor)
  drawHotelsPage(doc, data, cursor)
  drawEmergencyPage(doc, data, cursor)
  drawTipsPage(doc, data, cursor)

  addPageFooters(doc, true)
  return doc
}

export function downloadTravelGuidePdf(data: TravelGuideData): void {
  const doc = buildTravelGuidePdf(data)
  const slug = data.destinationName.toLowerCase().replace(/\s+/g, '-')
  doc.save(`waymark-travel-guide-${slug}.pdf`)
}
